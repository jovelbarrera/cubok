    <h2>Selección de los Trabajos</h2>
    <p class="text-justify">
        Cada uno de los artículos será evaluado por 3 revisores que asignarán un puntaje a cada uno de los artículos basados en los siguientes criterios:

    </p>
    <ol>
        <li>Adecuada revisión bibliográfica, buen desarrollo teórico, adecuada presentación de la metodología, análisis de los resultados, conclusiones coherentes con los resultados, bibliografía adecuada, buena redacción y que cumpla con el formato IEEE.</li>
        <li>Los artículos aceptados que tengan el mayor puntaje serán programados en agenda para presentarse en formato conferencia de 20 minutos. El resto de trabajos aprobados serán presentados en formato Póster, y expuestos durante la jornada de la tarde del segundo día de conferencias de CONESCAPAN XXXIV</li>
    </ol>
