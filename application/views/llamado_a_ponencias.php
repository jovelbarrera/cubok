    <h2>Envío de trabajos</h2>
    <p class="text-justify">
        Todos los trabajos que se presenten en CONESCAPAN XXXIV, deberán seguir las siguientes pautas:
    </p>
    <ol>
        <li>Los artículos completos deberán estar escritos en español o inglés.</li>
        <li>
            Los trabajos a evaluación deberán ser enviados en formato PDF, siguiendo el formato IEEE de doble columna para trabajos técnicos.<br/>
            Puede descargar la plantilla del siguiente enlace:<br/>
            <a href="http://www.ieee.org/conferences_events/conferences/publishing/templates.html">IEEE Manuscript Templates for Conference Proceedings</a>
        </li>
        <li>La extensión máxima del artículo será de 6 páginas.</li>
        <li>Todos los artículos deberán incluir al menos los siguientes apartados:
            <ol style="list-style: upper-roman">  
                <li>Título</li>
                <li>Nombre del autor o autores</li>
                <li>Institución o compañía</li>
                <li>E-mail de contacto</li>
                <li>Resumen</li>
                <li>Al menos 5 palabras clave</li>
                <li>Introducción especificando el problema estudiado y el estado del arte con una discusión bibliográfica.</li>
                <li>La parte principal del artículo deberá presentar la fundamentación en la que se apoya el estudio, la metodología utilizada, un análisis de la información o resultados. Esto deberá estar organizado de forma secuencial y bien estructurada, con resultados numéricos presentados por tablas o gráficos.</li>
                <li>Conclusiones</li>
                <li>Referencias</li>
                <li>Breve biografía del autor o autores.</li>
            </ol>
